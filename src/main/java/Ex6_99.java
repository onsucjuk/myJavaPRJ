public class Ex6_99 {
    public static void main(String[] args) {

        int i;
        int j;

        for (i = 2; i <= 9; i++) {

            for (j = 2; j <= 9; j++) {

                System.out.printf(" %d x %d = %d\n",i,j,i * j);

            }

            System.out.printf("\n");

        }
    }
}
